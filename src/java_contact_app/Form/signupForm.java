    public boolean verifData() {
        if (jTextFieldUsername.getText().equals("") || jTextFieldLName.getText().equals("") && jTextFieldFname.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "One or more field are empty");
            return false;
        } else if (jPasswordField2.equals(String.valueOf(jPasswordFieldPass.getText()))) {
            JOptionPane.showMessageDialog(null, "Inccorect");
            return false;
        } else if (imagepth == null) {
            JOptionPane.showMessageDialog(null, "No image selected");
            return false;
        } else {
            return true;
        }
    }

    public boolean isUsernameExist(String un) throws SQLException {
        boolean uExist = false;
        Connection con = null;
        try {
            con = myConnection.getConnection();
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(signupForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        PreparedStatement ps;
        ps = con.prepareStatement("SELECT * FROM `user` WHERE `Username` = ? ");
        ps.setString(1, jTextFieldUsername.getText());

        ResultSet rs;
        rs = ps.executeQuery();
        if (rs.next()) {
            uExist = true;
        }
        return uExist;
    }

