    private void jButtonCreateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonCreateMouseClicked
        Connection con = null;
        if (verifData()) {
            try {
                con = myConnection.getConnection();
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(signupForm.class.getName()).log(Level.SEVERE, null, ex);
            }
            PreparedStatement ps;
            try {
                ps = con.prepareStatement("INSERT INTO `user`(`Fname`, `Lname`, `Username`, `Password`, `Image`) VALUES (?,?,?,?,?)");
                ps.setString(1, jTextFieldFname.getText());
                ps.setString(2, jTextFieldLName.getText());
                ps.setString(3, jTextFieldUsername.getText());
                ps.setString(4, String.valueOf(jPasswordFieldPass.getPassword()));

                InputStream img = new FileInputStream(new File(imagepth));
                ps.setBlob(5, img);

                if (isUsernameExist(jTextFieldUsername.getText())) {
                    JOptionPane.showMessageDialog(null, "Username already exists");
                } else {
                    if (ps.executeUpdate() != 0) {
                        JOptionPane.showMessageDialog(null, "account created");
                    } else {
                        JOptionPane.showMessageDialog(null, "Something Wrong");
                    }
                }
            } catch (SQLException ex) {
                Logger.getLogger(signupForm.class.getName()).log(Level.SEVERE, null, ex);
            } catch (FileNotFoundException ex) {
                Logger.getLogger(signupForm.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButtonCreateMouseClicked

